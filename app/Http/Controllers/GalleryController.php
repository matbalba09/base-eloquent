     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(GalleryRequest $galleryRequest)
    {
        $validated = $galleryRequest->validated();

        $destinationPath = Response::GALLERY_IMAGE_PATH.'/'.$galleryRequest->name;
        $path = Helper::saveFile($galleryRequest->file('image'), $destinationPath, $galleryRequest->name);

        $gallery = Gallery::create([
            'name' => $galleryRequest->name,
            'path' => $path,
        ]);

        return $gallery;
    }

